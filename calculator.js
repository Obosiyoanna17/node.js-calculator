function add(num1, num2) {
    return (num1 + num2);
}
function substract(num3, num4) {
    return (num3 - num4);
}
function multiply(num5, num6) {
    return (num5 * num6);
}
function division   (num7, num8) {
    return (num7 / num8);
}

module.exports = {
    add,
    substract,
    multiply,
    division,
}